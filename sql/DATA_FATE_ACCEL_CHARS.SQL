        ID int NOT NULL AUTO_INCREMENT
        , SERVER VARCHAR(200)
        , CHARACTER_NAME VARCHAR(200)
        , CHARACTER_SHORT_NAME VARCHAR(200) UNIQUE
        , DEFAULT_DIE_CODE VARCHAR(20)
        , ASPECT_1 VARCHAR(200)
        , ASPECT_2 VARCHAR(200)
        , ASPECT_3 VARCHAR(200)
        , ASPECT_4 VARCHAR(200)
        , ASPECT_5 VARCHAR(200)
        , CAREFUL INT
        , CLEVER INT
        , FLASHY INT
        , FORCEFUL INT
        , QUICK INT
        , SNEAKY INT
        , FATE_POINTS INT
        , STRESS INT
        , CONSEQUENCE_2 VARCHAR(200)
        , CONSEQUENCE_4 VARCHAR(200)
        , CONSEQUENCE_6 VARCHAR(200)
        , PRIMARY KEY (ID)
