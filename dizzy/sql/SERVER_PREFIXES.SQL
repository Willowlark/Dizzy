DROP TABLE SERVER_PREFIXES;

CREATE TABLE SERVER_PREFIXES (
        ID int NOT NULL UNIQUE
        , SERVER_ID INT
        , PREFIX VARCHAR(20)
        , PRIMARY KEY (ID)
        , FOREIGN KEY (SERVER_ID) REFERENCES SERVERS(SERVER_ID)
);

